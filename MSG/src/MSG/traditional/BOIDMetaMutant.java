package MSG.traditional;

import MSG.MSGConstraints;

public class BOIDMetaMutant {
	public static int BOID(int exp, int op, String id) {
		int original = exp;

		if (op == MSGConstraints.U_BIT_NOT)
			original = ~exp;

		return original;
	}

	public static long BOID(long exp, int op, String id) {
		long original = exp;

		if (op == MSGConstraints.U_BIT_NOT)
			original = ~exp;

		return original;
	}
}
