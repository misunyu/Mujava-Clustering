package nujava.traditional;

import nujava.MutantMonitor;

public class AOIDUMetaMutant {

	public static int AOIDUGen(int exp, int op, int changePoint, String mutantID) {
		if (exp != 0) {
			String mID = mutantID + "_" + op;
			MutantMonitor.getInstance().updateSingleMutant(mID, changePoint);
		}

		return exp;
	}

	public static float AOIDUGen(float exp, int op, int changePoint,
			String mutantID) {
		if (exp != 0) {
			String mID = mutantID + "_" + op;
			MutantMonitor.getInstance().updateSingleMutant(mID, changePoint);
		}

		return exp;
	}

	public static long AOIDUGen(long exp, int op, int changePoint,
			String mutantID) {

		if (exp != 0) {
			String mID = mutantID + "_" + op;
			MutantMonitor.getInstance().updateSingleMutant(mID, changePoint);
		}

		return exp;
	}

	public static double AOIDUGen(double exp, int op, int changePoint,
			String mutantID) {

		if (exp != 0) {
			String mID = mutantID + "_" + op;
			MutantMonitor.getInstance().updateSingleMutant(mID, changePoint);
		}

		return exp;
	}
}
